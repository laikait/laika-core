declare(strict_types=1);

namespace Laika\Core\App;

use Twig\Loader\FilesystemLoader as Engine;
use Laika\Core\Service\Directory;
use Laika\Core\Service\Visitor;
use Laika\Core\Service\Request;
use Laika\Core\Service\Config;
use Laika\Core\Service\Local;
use Laika\Core\Service\File;
use Laika\Core\Service\Page;
use Laika\Core\Service\Url;
use Twig\Environment;
use Twig\TwigFilter;

class Template
{
    /**
     * @var object $twig
     */
    protected object $twig;

    /**
     * @var array $vars
     */
    protected array $vars = [];

    /**
     * @var ?string $templateDirectory Template Directory
     */
    protected ?string $templateDirectory;

    /**
     * @var ?string $cacheDirectory Template Cache Directory
     */
    protected ?string $cacheDirectory;

    /**
     * File Extension
     * @var string
     */
    protected string $extension;

    public function __construct(?string $templateSubDirectory = null, ?string $cacheSubDirectory = null)
    {
        // Ensure Template & Cache Paths
        $this->ensureTemplatePath($templateSubDirectory);
        $this->ensureCachePath($cacheSubDirectory);
        $this->extension = 'twig';

        // Run Template Engine
        $engine = new Engine($this->templateDirectory);
        $this->twig = new Environment($engine, [
            'debug' =>  Config::get('env', 'debug', true),
            'cache' =>  $this->cacheDirectory
        ]);

        // Assign Template Default Vars
        $this->defaultVars();
        // Assign Template Default Filters
        $this->defaultFilters();

        // Load Template Functions File
        $func_file = "{$this->templateDirectory}/loader.php";
        if (!File::exists($func_file)) {
            File::touch($func_file);
            File::write("<?php\n// Auto Generated By Framework\n// Start Code From Here...\n\n", $func_file);
        }

        File::require($func_file, true);
    }

    /**
     * Set File Extension
     * @param string $extension File Extension
     * @return void
     * @deprecated Use Template::html() Instead
     */
    public function extension(string $extension): void
    {
        $this->extension = $extension;
        return;
    }

    /**
     * Set HTML Extension
     * @return static
     */
    public function html(): static
    {
        $this->extension = 'html';
        return $this;
    }

    /**
     * Render View
     * @param string $name View Name
     * @return string Rendered View
     */
    public function view(string $name): string
    {
        return $this->twig->render("{$name}.{$this->extension}", $this->vars);
    }

    /**
     * Assign Variable
     * @param string|array $key Key Name or Array of Key, Value Pair
     * @param mixed $value Key Name or Array of Key, Value Pair
     */
    public function assign(string|array $key, mixed $value = null): void
    {
        if (is_string($key)) {
            $key = [$key => $value];
        }
        $this->vars = array_merge($this->vars, $key);
    }

    /**
     * @param string $name Filter Name
     * @param string|callable $callable Callable Filter
     * @return void
     */
    public function addFilter(string $name, string|callable $callable): void
    {
        $this->twig->addFilter(new TwigFilter($name, $callable));
    }

    /**
     * Get Assigned Vars
     * @return array
     */
    public function vars(): array
    {
        return $this->vars;
    }

    ##################################################################################
    /*-------------------------------- INTERNAL API --------------------------------*/
    ##################################################################################

    /**
     * Ensure Template Path
     * @param ?string $subdir Sub Directory Path
     * @return void
     */
    protected function ensureTemplatePath(?string $subdir = null): void
    {
        $subdir = $subdir ? trim($subdir, '/') : '';
        $this->templateDirectory = is_dir($subdir) ? $subdir : APP_PATH . "/lf-templates/{$subdir}";
        $this->templateDirectory = rtrim($this->templateDirectory, '/');
        Directory::make($this->templateDirectory);
    }

    /**
     * Ensure Cache Path
     * @param ?string $subdir Sub Directory Path
     * @return void
     */
    protected function ensureCachePath(?string $subdir = null): void
    {
        $subdir = $subdir ? trim($subdir, '/') : '';
        $this->cacheDirectory = is_dir($subdir) ? $subdir : APP_PATH . "/lf-cache/{$subdir}";
        $this->cacheDirectory = rtrim($this->cacheDirectory, '/');
        Directory::make($this->cacheDirectory);
    }

    /**
     * Assign Default Vars
     * @return void
     */
    protected function defaultVars()
    {
        // Assign Local Name. Default is en
        $this->assign('local', Local::get());
        // Assign Pagination
        $this->assign('page', ['number' => Page::number(), 'next' => Page::next(), 'previous' => Page::previous()]);
        // Assign Request Inputs
        $this->assign('input', new InputHandler());
        // Assign Form Errors
        $this->assign('errors', Request::errors());
        // Visitor Info
        $this->assign('visitor', Visitor::info());
    }

    /**
     * Assign Default Filters
     * @return void
     */
    protected function defaultFilters()
    {
        // Register Hooks
        $this->addFilter('hook', 'apply_hook');
        // Decode Html Special Characters
        $this->addFilter('decode', 'htmlspecialchars_decode');
        // Register Slugs
        $this->addFilter('slug', function (int $index){ return Url::segment($index); });
        // Register Queries
        $this->addFilter('query', function (string $key) { return Url::query($key); });
        // Register Named
        $this->addFilter('named', function(string $name, array $params = []){
            return named($name, $params, true);
        });
        // Register Asset
        $this->addFilter('asset', 'asset');
    }
}

// Input Handler
final class InputHandler
{
    /**
     * Check Property Exists
     * @param mixed $name
     * @return bool
     */
    public function __isset($name): bool
    {
        return true;
    }

    /**
     * Get Input Value From Key
     * @param mixed $name
     * @throws \Exception
     * @return string
     */
    public function __get($name): string
    {
        $input = Request::input($name);
        return is_string($input) ? $input : '';
    }

    public function __call($name, $arguments)
    {
        $input = Request::input($name);
        if (is_array($input)) {
            return $input[(int) ($arguments[0] ?? 0)] ?? '';
        }
        return '';
    }
}